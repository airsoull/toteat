const addData = (chart, label, data) => {
  chart.data.labels.push(label);
  chart.data.datasets.forEach((dataset) => {
    dataset.data.push(data);
  });
  chart.update();
}

const chartData = async () => {
  const headers = new Headers({
    'Content-Type': 'x-www-form-urlencoded',
    'X-CSRFToken': getCookie('csrftoken')
  });
  const loadingDiv = document.querySelector('#loading-div');
  const loadingSpinnerDiv = document.querySelector('#loading-spinner-div');

  await fetch('/api/sales/')
    .then(response => response.json())
    .then((data) => {
      !data.total_sales && loadingSpinnerDiv.classList.remove('d-none');
      data.months.forEach(
        (values) => addData(myLineChart, values.month, values.value)
      );
    });

  await fetch('/api/sales/data/', { method: 'POST', headers })
    .then(response => response.json())
    .then(data => {
      console.log(data.need_reload);
      data.need_reload && loadingDiv.classList.remove('d-none');
    });
};

!!ctx && chartData();
